let currentLang = 'en';

document.addEventListener('DOMContentLoaded', () => {
    // === Language Selector Logic ===
    const langBtns = document.querySelectorAll('.lang-btn');

    function updateLanguage(lang) {
        currentLang = lang;
        
        // Update document title
        if (document.title !== undefined) {
            document.title = translations[lang]["title"];
        }

        // Update translation texts
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                el.innerHTML = translations[lang][key];
            }
        });

        // Update meta description
        const metaDesc = document.querySelector('meta[data-i18n-desc]');
        if (metaDesc) {
            const key = metaDesc.getAttribute('data-i18n-desc');
            if (translations[lang] && translations[lang][key]) {
                metaDesc.setAttribute('content', translations[lang][key]);
            }
        }
    }

    langBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            // Update active state
            langBtns.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            
            // Apply translation
            const lang = btn.getAttribute('data-lang');
            updateLanguage(lang);
        });
    });

    // === Sticky Navbar on Scroll ===
    const navbar = document.getElementById('navbar');
    
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            navbar.classList.add('scrolled');
        } else {
            navbar.classList.remove('scrolled');
        }
    });

    // === Smooth Scroll & Active Nav Link Updates ===
    const sections = document.querySelectorAll('section');
    const navLinks = document.querySelectorAll('.nav-link');

    window.addEventListener('scroll', () => {
        let current = '';
        
        sections.forEach(section => {
            const sectionTop = section.offsetTop;
            const sectionHeight = section.clientHeight;
            if (scrollY >= (sectionTop - sectionHeight / 3)) {
                current = section.getAttribute('id');
            }
        });

        navLinks.forEach(link => {
            link.classList.remove('active');
            if (link.getAttribute('href') === `#${current}`) {
                link.classList.add('active');
            }
        });
    });

    // === Intersection Observer for Fade-in Animations ===
    const fadeElements = document.querySelectorAll('.fade-in');
    
    const appearOptions = {
        threshold: 0.15,
        rootMargin: "0px 0px -50px 0px"
    };

    const appearOnScroll = new IntersectionObserver(function(entries, observer) {
        entries.forEach(entry => {
            if (!entry.isIntersecting) {
                return;
            } else {
                entry.target.classList.add('visible');
                observer.unobserve(entry.target);
            }
        });
    }, appearOptions);

    fadeElements.forEach(fader => {
        appearOnScroll.observe(fader);
    });

    // === Dynamic Live Rating System ===
    const ratingSystems = document.querySelectorAll('.rating-system');

    ratingSystems.forEach(system => {
        const stars = system.querySelectorAll('.stars i');
        const ratingText = system.querySelector('.rating-text');
        
        let currentRating = 0;
        
        // Initial setup based on pre-filled classes (if any, although HTML has fixed rating initially)
        // Instead, let's treat the pre-filled HTML as standard and allow user override
        const activeStarsList = system.querySelectorAll('.stars i.ph-fill');
        currentRating = activeStarsList.length;

        stars.forEach((star, index) => {
            // Hover effect
            star.addEventListener('mouseenter', () => {
                resetStars();
                highlightStars(index);
            });

            // Remove hover effect when mouse leaves
            star.addEventListener('mouseleave', () => {
                resetStars();
                fillStars(currentRating - 1); // Fill up to current real rating
            });

            // Click to lock in rating
            star.addEventListener('click', () => {
                currentRating = index + 1;
                fillStars(currentRating - 1);
                ratingText.textContent = `${translations[currentLang]["rating_thanks"]}${currentRating}.0`;
                ratingText.style.color = 'var(--kz-yellow-dark)';
                
                // Add a little pop animation
                star.style.transform = 'scale(1.2)';
                setTimeout(() => star.style.transform = 'scale(1)', 200);
            });
        });

        function resetStars() {
            stars.forEach(s => {
                s.classList.remove('ph-fill');
                s.classList.remove('hover-active');
                s.classList.add('ph');
            });
        }

        function highlightStars(index) {
            for (let i = 0; i <= index; i++) {
                stars[i].classList.remove('ph');
                stars[i].classList.add('ph-fill');
                stars[i].classList.add('hover-active');
            }
        }

        function fillStars(index) {
            resetStars();
            for (let i = 0; i <= index; i++) {
                stars[i].classList.remove('ph');
                stars[i].classList.add('ph-fill');
            }
        }
    });
});
